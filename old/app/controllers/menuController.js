const { changeView } = require('../services/viewService');
const viewEnum = require('../enums/viewEnum');

/////////////////////////////////////////////////////////////////////
// Init
/////////////////////////////////////////////////////////////////////

function init() {
    setupMenuEvents();
}

/////////////////////////////////////////////////////////////////////
// Button Events
/////////////////////////////////////////////////////////////////////

function setupMenuEvents() {
    $('#profile-menu').on('click', btnProfileMenuEvent);
    $('#instances-menu').on('click', btnInstancesMenuEvent);
}

function btnProfileMenuEvent() {
    changeView(viewEnum.profileView);
}

function btnInstancesMenuEvent() {
    changeView(viewEnum.instanceView);
}

/////////////////////////////////////////////////////////////////////
// Exports
/////////////////////////////////////////////////////////////////////

module.exports = {
    init
};